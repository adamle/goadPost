import UIKit

extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4037566554, green: 0.7161955114, blue: 0.4686756243, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.6003380566, green: 0.865584788, blue: 0.6334497222, alpha: 1)
    }
}
